    @Test
    @Transactional
    public void searchForUserHaveResult() throws Exception {
        // Initialize the database
        productRepository.saveAndFlush(product);
        
        Product productNotMatchName = createEntityNotMatchName();
        productRepository.saveAndFlush(productNotMatchName);

        // Get all the productList
        restProductMockMvc.perform(get("/api/products/search").param("keyword", "A"))
            .andExpect(status().isOk())
            .andExpect(content().contentType(MediaType.APPLICATION_JSON_UTF8_VALUE))
            .andExpect(jsonPath("$.[*].id").value(hasItem(product.getId().intValue())))
            .andExpect(jsonPath("$.[*].id").value(hasItem(not(productNotMatchName.getId().intValue()))))
            .andExpect(jsonPath("$.[*].name").value(hasItem(DEFAULT_NAME.toString())))
            .andExpect(jsonPath("$.[*].price").value(hasItem(DEFAULT_PRICE.intValue())))
            .andExpect(jsonPath("$.[*].quantity").value(hasItem(DEFAULT_QUANTITY)))
            .andExpect(jsonPath("$.[*].visible").value(hasItem(DEFAULT_VISIBLE.booleanValue())));
    }

	private Product createEntityNotMatchName() {
		Product productNotMatchName = createEntity(em);
        productNotMatchName.setName(UPDATED_NAME);
		return productNotMatchName;
	}

